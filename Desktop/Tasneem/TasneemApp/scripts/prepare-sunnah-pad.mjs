import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

const sourceDir = path.join(projectRoot, 'assets', 'sunnah', 'editions');
const targetDir = path.join(projectRoot, 'android', 'sunnahpack', 'src', 'main', 'assets', 'sunnah', 'editions');
const mapFile = path.join(projectRoot, 'app', 'constants', 'sunnahEditionAssetMap.js');

const copyDirectoryRecursive = async (fromDir, toDir) => {
  await fs.mkdir(toDir, { recursive: true });
  const entries = await fs.readdir(fromDir, { withFileTypes: true });

  for (const entry of entries) {
    const fromPath = path.join(fromDir, entry.name);
    const toPath = path.join(toDir, entry.name);

    if (entry.isDirectory()) {
      await copyDirectoryRecursive(fromPath, toPath);
      continue;
    }

    await fs.copyFile(fromPath, toPath);
  }
};

const writePadOnlyMap = async () => {
  const content = [
    '// Auto-generated by scripts/prepare-sunnah-pad.mjs',
    '// PAD-only mode: no bundled Sunnah editions are required in JS bundle.',
    '',
    'export const getSunnahCatalogAsset = () => null;',
    '',
    'export const SUNNAH_EDITION_ASSET_MAP = {};',
    '',
  ].join('\n');

  await fs.writeFile(mapFile, content, 'utf8');
};

const main = async () => {
  const sourceInfo = await fs.stat(sourceDir).catch(() => null);
  if (!sourceInfo?.isDirectory()) {
    throw new Error(`Source directory not found: ${sourceDir}`);
  }

  await fs.rm(targetDir, { recursive: true, force: true });
  await copyDirectoryRecursive(sourceDir, targetDir);
  process.stdout.write(`Copied Sunnah editions to ${path.relative(projectRoot, targetDir)}\n`);

  await writePadOnlyMap();
  process.stdout.write(`Updated ${path.relative(projectRoot, mapFile)} (PAD-only map)\n`);

  await fs.rm(sourceDir, { recursive: true, force: true });
  process.stdout.write(`Removed ${path.relative(projectRoot, sourceDir)} from bundled assets\n`);

  process.stdout.write('PAD preparation complete.\n');
};

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
