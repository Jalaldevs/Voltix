import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

const BASE_URL = 'https://cdn.jsdelivr.net/gh/fawazahmed0/hadith-api@1';
const BOOKS = [
  'bukhari',
  'nasai',
  'abudawud',
  'muslim',
  'tirmidhi',
  'ibnmajah',
  'malik',
  'nawawi',
  'qudsi',
];
const SUPPORTED_SUNNAH_LANGUAGE_CODES = ['eng', 'fra', 'urd', 'tur', 'ind', 'ben', 'rus', 'tam'];

const editionsDir = path.join(projectRoot, 'assets', 'sunnah', 'editions');
const mapFile = path.join(projectRoot, 'app', 'constants', 'sunnahEditionAssetMap.js');

const fetchText = async (url) => {
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`);
  }
  return res.text();
};

const writeAssetMap = async (editionKeys) => {
  const lines = [];
  lines.push('// Auto-generated by scripts/sync-sunnah-assets.mjs');
  lines.push('// Do not edit manually.');
  lines.push('');
  lines.push("export const getSunnahCatalogAsset = () => require('../../assets/sunnah/editions/editions.json');");
  lines.push('');
  lines.push('export const SUNNAH_EDITION_ASSET_MAP = {');
  for (const key of editionKeys) {
    lines.push(`  '${key}': () => require('../../assets/sunnah/editions/${key}.min.json'),`);
  }
  lines.push('};');
  lines.push('');
  await fs.writeFile(mapFile, lines.join('\n'), 'utf8');
};

const main = async () => {
  await fs.mkdir(editionsDir, { recursive: true });

  const catalogText = await fetchText(`${BASE_URL}/editions.json`);
  const catalog = JSON.parse(catalogText);
  await fs.writeFile(path.join(editionsDir, 'editions.json'), catalogText, 'utf8');
  process.stdout.write('Saved editions.json\n');

  const targets = [
    ...BOOKS.map((book) => ({
      key: `ara-${book}`,
      fileName: `ara-${book}.min.json`,
      url: `${BASE_URL}/editions/ara-${book}.min.json`,
    })),
  ];

  for (const book of BOOKS) {
    const entries = catalog?.[book]?.collection || [];
    const availableCodes = [...new Set(
      entries
        .filter((item) => item.language !== 'Arabic')
        .map((item) => {
          const code = String(item.name || '').split('-')[0];
          return SUPPORTED_SUNNAH_LANGUAGE_CODES.includes(code) ? code : null;
        })
        .filter(Boolean)
    )];

    for (const code of availableCodes) {
      targets.push({
        key: `${code}-${book}`,
        fileName: `${code}-${book}.min.json`,
        url: `${BASE_URL}/editions/${code}-${book}.min.json`,
      });
    }
  }

  for (const target of targets) {
    const content = await fetchText(target.url);
    const filePath = path.join(editionsDir, target.fileName);
    await fs.writeFile(filePath, content, 'utf8');
    process.stdout.write(`Saved ${target.fileName}\n`);
  }

  const editionKeys = targets.map((target) => target.key);

  await writeAssetMap(editionKeys);
  process.stdout.write(`Updated ${path.relative(projectRoot, mapFile)}\n`);
  process.stdout.write(`Done. Total files: ${targets.length}\n`);
};

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
